int main (void)
{ uint16 distance;
uint8 RED_LED=0;
uint8 GREEN_LED=1;
uint8 BLUE_LED=2;
	LCD_init();
	 Ultrasonic_init();
	 LEDS_init();
	 Buzzer_init();

	 SREG|=(1<<7);
	 LCD_displayString("DISTANCE=");
	 while(1)
	 {
		distance=Ultrasonic_readDistance();

		LCD_moveCursor(0,9);
					if(distance>=100)
									{
										LCD_intgerToString(distance);
									}
									else{
									LCD_intgerToString(distance);
									LCD_displayCharacter(' ');}
					LCD_moveCursor(0,12);

					LCD_displayString("cm");
if(distance<=5){
	LCD_moveCursor(1,5);
	LCD_displayString("STOP");
	Buzzer_on();
	LED_on(RED_LED);
	LED_on(GREEN_LED);
	LED_on(BLUE_LED);
	_delay_ms(500);
	LED_off(RED_LED);
	LED_off(GREEN_LED);
	LED_off(BLUE_LED);
	_delay_ms(500);




}
else if ((distance>=6)&&(distance<=10))
{
	LED_on(RED_LED);
		LED_on(GREEN_LED);
		LED_on(BLUE_LED);
		Buzzer_off();
		LCD_moveCursor(1,5);
			LCD_displayString("                        ");
}
else if ((distance>=11)&&(distance<=15))
{
	LED_on(RED_LED);
			LED_on(GREEN_LED);
			LED_off(BLUE_LED);
			Buzzer_off();
			LCD_moveCursor(1,5);
					LCD_displayString("                        ");
}
else if ((distance>=16)&&(distance<=20))

{
	LED_on(RED_LED);
				LED_off(GREEN_LED);
				LED_off(BLUE_LED);
				Buzzer_off();
				LCD_moveCursor(1,5);
							LCD_displayString("                        ");
}
else
{
	LED_off(RED_LED);
					LED_off(GREEN_LED);
					LED_off(BLUE_LED);
					Buzzer_off();
					LCD_moveCursor(1,5);
								LCD_displayString("                        ");
}

	 }
}























































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































